# Get the product of five numbers passed in as a string
def GetProductOf5(NumberSlice):
    return int(NumberSlice[0]) * int(NumberSlice[1]) * \
           int(NumberSlice[2]) * int(NumberSlice[3]) * \
           int(NumberSlice[4])

# Find the greatest product of 5 consecutive digits in a number
def FindGreatestProductOf5(Integer):
    Temp = 0
    GreatestProduct = 0

    # Loop over each combination of 5 consecutive digits
    for I in range(5, len(Integer)):
        # If the current digit is 0, skip over it
        if Integer[I] == 0:
            continue
        else:
            Temp = GetProductOf5(Integer[I-5:I])
            if Temp > GreatestProduct:
                GreatestProduct = Temp

    # Return the greatest product of 5 consecutive digits
    return GreatestProduct

